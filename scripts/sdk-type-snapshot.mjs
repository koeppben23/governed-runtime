/**
 * SDK Type Baseline Snapshot Tool.
 *
 * Compares the installed @opencode-ai/plugin type definitions against
 * committed baseline files. Fails with exit 1 if any drift is detected.
 *
 * Usage:
 *   node scripts/sdk-type-snapshot.mjs                      # Check opencode (default)
 *   node scripts/sdk-type-snapshot.mjs --update             # Update opencode baseline
 *   node scripts/sdk-type-snapshot.mjs --platform opencode  # Explicit platform
 *   node scripts/sdk-type-snapshot.mjs --platform claude-code --update
 *   node scripts/sdk-type-snapshot.mjs --platform codex --update
 *   node scripts/sdk-type-snapshot.mjs --platform mcp --update
 *   node scripts/sdk-type-snapshot.mjs --platform governance --update
 *
 * Baseline directory structure:
 *   .sdk-baselines/opencode/       — OpenCode plugin type definitions
 *   .sdk-baselines/claude-code/    — Claude Code hook protocol schemas
 *   .sdk-baselines/codex/          — Codex hook protocol schemas
 *   .sdk-baselines/mcp/            — MCP tool input schemas (per-tool)
 *   .sdk-baselines/governance/     — HAI governance surface schemas
 *
 * Source files (opencode):
 *   node_modules/@opencode-ai/plugin/dist/index.d.ts
 *   node_modules/@opencode-ai/plugin/dist/tool.d.ts
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createHash } from 'node:crypto';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

// ── Platform Definitions ─────────────────────────────────────────────────────

const PLATFORMS = {
  opencode: {
    files: [
      {
        source: path.join(root, 'node_modules', '@opencode-ai', 'plugin', 'dist', 'index.d.ts'),
        baseline: path.join(root, '.sdk-baselines', 'opencode', 'plugin-index.d.ts'),
        label: 'plugin/dist/index.d.ts',
      },
      {
        source: path.join(root, 'node_modules', '@opencode-ai', 'plugin', 'dist', 'tool.d.ts'),
        baseline: path.join(root, '.sdk-baselines', 'opencode', 'plugin-tool.d.ts'),
        label: 'plugin/dist/tool.d.ts',
      },
    ],
    versionSource: path.join(root, 'node_modules', '@opencode-ai', 'plugin', 'package.json'),
    versionFile: path.join(root, '.sdk-baselines', 'opencode', 'version.json'),
  },
  'claude-code': {
    files: [], // JSON schema files — no source comparison, generated by tests
    versionFile: path.join(root, '.sdk-baselines', 'claude-code', 'version.json'),
  },
  codex: {
    files: [], // JSON schema files — no source comparison, generated by tests
    versionFile: path.join(root, '.sdk-baselines', 'codex', 'version.json'),
  },
  mcp: {
    files: [], // JSON schema files — generated from Zod tool schemas
    versionFile: path.join(root, '.sdk-baselines', 'mcp', 'version.json'),
  },
  governance: {
    files: [], // JSON schema files — generated from HAI interface
    versionFile: path.join(root, '.sdk-baselines', 'governance', 'version.json'),
  },
};

// ── Helpers ──────────────────────────────────────────────────────────────────

/**
 * Normalize content for comparison: convert CRLF to LF, trim trailing
 * whitespace per line, ensure single trailing newline.
 */
function normalize(content) {
  return (
    content
      .replace(/\r\n/g, '\n')
      .split('\n')
      .map((line) => line.trimEnd())
      .join('\n')
      .replace(/\n+$/, '') + '\n'
  );
}

function sha256(content) {
  return createHash('sha256').update(content).digest('hex');
}

/**
 * Produce a minimal unified-diff-style output showing which lines changed.
 */
function simpleDiff(label, baselineContent, sourceContent) {
  const baseLines = baselineContent.split('\n');
  const srcLines = sourceContent.split('\n');
  const lines = [];
  const maxLen = Math.max(baseLines.length, srcLines.length);
  for (let i = 0; i < maxLen; i++) {
    const bl = baseLines[i] ?? '';
    const sl = srcLines[i] ?? '';
    if (bl !== sl) {
      lines.push(`  Line ${i + 1}:`);
      lines.push(`    - ${bl}`);
      lines.push(`    + ${sl}`);
    }
  }
  return [`--- ${label} (baseline)`, `+++ ${label} (installed)`, ...lines].join('\n');
}

// ── Argument Parsing ─────────────────────────────────────────────────────────

function parsePlatformArg() {
  const idx = process.argv.indexOf('--platform');
  if (idx === -1) return 'opencode';
  const value = process.argv[idx + 1];
  if (!value || !Object.keys(PLATFORMS).includes(value)) {
    console.error(`ERROR: Invalid --platform value: ${value}`);
    console.error(`Valid platforms: ${Object.keys(PLATFORMS).join(', ')}`);
    process.exit(1);
  }
  return value;
}

// ── Main ─────────────────────────────────────────────────────────────────────

function run() {
  const isUpdate = process.argv.includes('--update');
  const platform = parsePlatformArg();
  const config = PLATFORMS[platform];

  if (!config) {
    console.error(`ERROR: Unknown platform: ${platform}`);
    process.exit(1);
  }

  const FILES = config.files;

  // For platforms without source-comparison files (schema-only), skip file checks
  if (FILES.length === 0) {
    if (isUpdate) {
      console.log(`Platform "${platform}" uses schema-generated baselines.`);
      console.log(`Run the contract tests with --update to regenerate.`);
    } else {
      // Check that baseline directory exists
      const baseDir = path.join(root, '.sdk-baselines', platform);
      if (!fs.existsSync(baseDir)) {
        console.error(`ERROR: Baseline directory missing: .sdk-baselines/${platform}/`);
        console.error(`Run the SDK contract tests to generate baselines.`);
        process.exit(1);
      }
      console.log(`OK: .sdk-baselines/${platform}/ exists (schema baselines managed by tests).`);
    }
    process.exit(0);
  }

  // ── Pre-flight: source files must exist ────────────────────────────────────
  for (const f of FILES) {
    if (!fs.existsSync(f.source)) {
      console.error(`ERROR: Source file not found: ${f.source}`);
      console.error('Run "npm ci" to install dependencies first.');
      process.exit(1);
    }
  }

  if (isUpdate) {
    // ── Update mode ──────────────────────────────────────────────────────────
    for (const f of FILES) {
      const content = normalize(fs.readFileSync(f.source, 'utf-8'));
      fs.mkdirSync(path.dirname(f.baseline), { recursive: true });
      fs.writeFileSync(f.baseline, content, 'utf-8');
      console.log(`Updated: ${path.relative(root, f.baseline)} (${sha256(content).slice(0, 12)})`);
    }

    // Write version metadata
    let version = 'unknown';
    if (config.versionSource) {
      try {
        const pkg = JSON.parse(fs.readFileSync(config.versionSource, 'utf-8'));
        version = pkg.version;
      } catch {
        /* best-effort */
      }
    }
    const meta = {
      platform,
      updated: new Date().toISOString(),
      version,
      files: FILES.map((f) => ({
        label: f.label,
        hash: sha256(normalize(fs.readFileSync(f.source, 'utf-8'))),
      })),
    };
    fs.mkdirSync(path.dirname(config.versionFile), { recursive: true });
    fs.writeFileSync(config.versionFile, JSON.stringify(meta, null, 2) + '\n', 'utf-8');
    console.log(`Baseline updated for platform "${platform}" @ ${version}`);
    process.exit(0);
  }

  // ── Check mode ─────────────────────────────────────────────────────────────
  let drifted = false;
  for (const f of FILES) {
    if (!fs.existsSync(f.baseline)) {
      console.error(`ERROR: Baseline file missing: ${path.relative(root, f.baseline)}`);
      console.error(
        `Run "node scripts/sdk-type-snapshot.mjs --platform ${platform} --update" to create the baseline.`,
      );
      drifted = true;
      continue;
    }

    const sourceContent = normalize(fs.readFileSync(f.source, 'utf-8'));
    const baselineContent = normalize(fs.readFileSync(f.baseline, 'utf-8'));

    if (sourceContent !== baselineContent) {
      console.error(`DRIFT DETECTED: ${f.label}`);
      console.error(`  Baseline hash: ${sha256(baselineContent).slice(0, 12)}`);
      console.error(`  Installed hash: ${sha256(sourceContent).slice(0, 12)}`);
      console.error('');
      console.error(simpleDiff(f.label, baselineContent, sourceContent));
      console.error('');
      drifted = true;
    } else {
      console.log(`OK: ${f.label} (${sha256(sourceContent).slice(0, 12)})`);
    }
  }

  if (drifted) {
    console.error('');
    console.error('SDK type drift detected. Review the changes above, then run:');
    console.error(`  node scripts/sdk-type-snapshot.mjs --platform ${platform} --update`);
    process.exit(1);
  }

  console.log(`All SDK type baselines match for platform "${platform}".`);
  process.exit(0);
}

run();
